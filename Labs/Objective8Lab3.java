public class Objective8Lab3 {
  public static void main(String[] args) {
        
        for (int counter = 1; counter <= 20; counter++) {
            if (counter % 2 == 0) {
              
                System.out.println(counter + " is even");
            } else {
                
                System.out.println(counter + " is odd");
            }
        }
    }
}